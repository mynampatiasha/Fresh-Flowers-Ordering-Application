<h1>Changing The Water In Your Flower Arrangement</h1>

   <li> First remove any dead or dying flowers from the arrangement.</li>
   <li> After carefully removing the good flowers, clean the vase thoroughly with soapy water to remove any bacteria that could cause the fresh flowers to deteriorate even quicker. Be sure to rinse thoroughly.
    Replace the water and mix in the flower preservative provided by your florist, according to the instructions on the packet.</li>
   <li> For best results, cut stems with a sharp knife at an angle about one to two inches from the bottom. This allows them to better absorb water. Do not use scissors to cut your flowers because they can crush the stems and prevent water absorption.</li>
    <li>Place loose stems or wrapped bouquets of fresh flowers in your water mixture as soon as possible.</li>
