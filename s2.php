<h1 style = "color:red">Product Care</h1><br><br>
<h1><ul>You just had flowers delivered to you, what should I do?</h1><ul>

   <li> Keep fresh flowers away from drafts and extreme temperatures, which can quickly dry out the flowers and cause wilting.</li>
    <li>If your flowers came in plastic, remove this as soon as possible.
    Do not place your arrangement near fruit or in the path of cigarette smoke. The ethylene gas is detrimental to many flower types.</li>
   <li> Avoid placing flower arrangements in window sills and other areas with full sun where flowers can wilt due to overheating.</li>
    <li>Most flowers will last longer under cool conditions.</li>


<h1>How To Keep Your Flowers Looking Fresh</h1>
<li>Keep your vase filled with water! All flower and foliage stems should be submerged. Flowers stay fresher, longer when they can get a drink!</li>
<li>If your flowers came in a basket or other container with foam, add fresh water every day.</li>
<li>Immediately remove dead or wilting leaves and stems from fresh flower arrangements.</li>
